    <h1>Mision</h1>
    <p>Nuestro objetivo es construir Mostrar a los interesados (dueño de la empresa – usuarios, empleados) los requisitos funcionales y no funcionales que me permiten modelar los casos de uso las funciones del sistema junto con los actores de acuerdo a sus funciones para posteriormente desarrollar el diseño del desarrollo del sistema de información y a quien va dirigido desarrollo del sistema de información IM Villavicencio SAS, el cual es aplicable a todas las empresas que funcionen como intermediarias ante las empresas prestadoras de salud y sus afiliados, automatizando específicamente los procesos, novedades de personal, afiliaciones e incapacidades para lograr una mejor organización, agilidad y eficiencia, frente a las necesidades más sentidas de los afiliados. </p>

    <h1>Vision</h1>
    <p>La empresa IM Villavicencio SAS facilita procesos de afiliación a personas que no se encuentra vinculadas al sistema de salud y brinda asesoría jurídica en cuanto al proceso de reclamaciones e incapacidades a personas independientes. Durante la etapa de diagnóstico, se evidenciaron inconvenientes en tres procesos administrativos: los procesos de afiliación, asesoría y acompañamiento jurídico, se están adelantando en físico, lo cual genera pérdida de información, dificultad en el acceso a la misma, desorganización y fallas en el seguimiento. Por otro lado, la gestión administrativa de su propio personal, presenta inconsistencias: no hay soportes formales que evidencien el pago de la nómina, la empresa cuenta con una base de datos con información limitada e incompleta que no permite hacer un adecuado seguimiento del desempeño de sus empleados Lo que queremos hacer es: Desarrollar un sistema de información que contribuya a mejoramiento de los procesos administrativos de la empresa IM Villavicencio SAS, es por esto que estamos apuntando a estos tres objetivos específicos que son: 1) Gestionar el proceso administrativo de personal de la empresa (módulo gestión de personal). 2) Gestionar los procesos de afiliación a la EPS de personas independientes (módulo afiliaciones). 3) Gestionar solicitudes de incapacidades de los afiliados (módulo reclamaciones e incapacidades).</p>

